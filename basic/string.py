
# 字符串索引，与子字符串
var1 = 'Hello World!'
var2 = "Runoob"

print ("var1[0]: ", var1[0])
print ("var2[1:5]: ", var2[1:5])

# 字符串连接
var1 = 'Hello World!'

print ("已更新字符串 : ", var1[:6] + 'Runoob!')

